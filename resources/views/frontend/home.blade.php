            <p>sabemos.org es una organización de la sociedad civil y una plataforma de datos abiertos y confiables para que OSC, empresas y administraciones de gobierno puedan tomar mejores decisiones sobre cómo apoyar e incidir en comunidades con distintos proyectos a mediano y/o largo plazo.</p>
			<p>Estamos trabajando en generar una red de confianza. De este modo, cuidaremos que la información esté corroborada desde la fuente y agilizaremos el proceso para que los facilitadores de recursos orienten estos de manera oportuna mediante alianzas estratégicas para que quienes conforman la red de confianza puedan seguir trabajando en sus proyectos a mediano y largo plazo.</p>
			<p>Por otro lado, queremos establecer alianzas operativas con universidades, OCS y/o empresas con iniciativas que en el campo del conocimiento y el desarrollo tecnológico que puedan ayudar a fortalecer y alcanzar los objetivos que perseguimos.</p>
			<p>En sabemos.org creemos firmemente en estas formas de alianza. Estamos apostando por un modelo de trabajo que funja como intermediario entre los que actúan y entre los que tienen la capacidad de proveer los recursos para generar un verdadero impacto en el mediano y largo plazo. Queremos demostrar que los jóvenes organizados y comprometidos podemos seguir participando en la construcción de un país que ponga sus cimientos en la cooperación, solidaridad y compromiso.</p>
